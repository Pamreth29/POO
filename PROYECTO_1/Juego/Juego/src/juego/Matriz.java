        //CODIGO DE LA MATRIZ
        JFrame frame = new JFrame("Matriz Grid 25x25");
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        frame.setLayout(new GridLayout(25, 25));

        JTextField[][] textFieldMatrix = new JTextField[25][25];

        for (int i = 0; i < 25; i++) {
            for (int j = 0; j < 25; j++) {
                textFieldMatrix[i][j] = new JTextField();
                frame.add(textFieldMatrix[i][j]);
            }
        }

        // Establecer el tamaÃ±o de la ventana en 800x600
        frame.setSize(800, 600);

